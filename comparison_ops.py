#comparison operators

a = 45
b = 56
c = 78

#equal to
#==
var_1 = (a == b)
print(var_1)

var_2 = (b == c)
print(var_2)

#greater than
#>
var_g = (a > b)
var_g1 = (b > c)
var_g2 = (c > a)
print(var_g)
print(var_g1)
print(var_g2)

#less than
#<
var_l = (a < b)
var_l1 = (b < c)
var_l2 = (c < a)
print(var_l)
print(var_l1)
print(var_l2)

#not equal to
var_n = (a != b)
var_n1 = (b != c)
var_n3 = (c != a)
print(var_n)
print(var_n1)
print(var_n3)

#less than or equal to
var_r = (a <= b)
var_r1 = (b <= c)
var_r3 = (c <= a)
print(var_r)
print(var_r1)
print(var_r3)

#greater than or equal to
var_s = (a >= b)
var_s1 = (b >= c)
var_s3 = (c >= a)
print(var_s)
print(var_s1)
print(var_s3)

